package org.collectionspace.toronto1.freemarker;

public class FreemarkerException extends Exception {
	public FreemarkerException() { super(); }
	public FreemarkerException(String message) { super(message); }
	public FreemarkerException(Throwable cause) { super(cause); }
	public FreemarkerException(String message, Throwable cause) { super(message, cause); }
}
